package model;

public class Book {
	
	public String name;
	public String authorName;
	public String isbn;
	public String type;
	
	public Book() {}
	
	public Book(String name, String authorName, String isbn, String type) {
		this.name = name;
		this.authorName = authorName;
		this.isbn = isbn;
		this.type = type;
	}
	
	

}
